#------------------------------------ITERABLES-------------------------------------------------------

#CADENA--------------------------------------------------------------------------------------------
frutas = ('Manzana')

for fruta in frutas:
    print(f'Tu fruta es: {fruta}')

#Lista---------------------------------------------------------------------------------------------

frutas1 = (['Manzana','Fresas','Peras'])

for fruta1 in frutas1:
    print(f'Tu fruta es: {fruta1}')

#TUPA----------------------------------------------------------------------------------------------

frutas2 = (('Manzana','Fresa','Peras'))

for fruta2 in frutas2:
     print(f'Tu fruta es: {frutas2}')

#CONJUNTO------------------------------------------------------------------------------------------

frutas3 = ({'Manzana','Fresa','Peras'})

for fruta3 in frutas3:
     print(f'Tu fruta es: {frutas3}')

#DICCIONARIO---------------------------------------------------------------------------------------
frutas4 = ({'fruta1': 'Manzana','fruta2': 'Fresa'})
for fruta4 in frutas4:
    print(f'Tu fruta es: {frutas4}')



